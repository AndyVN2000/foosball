    @Override
    public Player save(Player player) {
        System.out.println("Adding a new player");
        players.add(player);
        System.out.println("Number of players after saving: " + players.size());
        return player;
    }

    @Override
    public Optional<Player> findByName(String name) {
        return players.stream()
            .filter(player -> name.equals(player.getName()))
            .findAny();
    }

    @Override
    public Optional<Player> findByInitials(String initials) {
        return players.stream()
            .filter(player -> initials.equals(player.getInitials()))
            .findAny();
    }

